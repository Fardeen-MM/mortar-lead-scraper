// ---------------------------------------------------------------------------
// Build lookup structures (lazy init)
// ---------------------------------------------------------------------------
let _prefixes = null;
let _byState = null;
let _byPrefix = null;

function _buildPrefixes() {
  if (_prefixes) return;
  _prefixes = ZIP_PREFIX_DATA.map(([prefix, lat, lng, city, state]) => ({
    prefix,
    lat,
    lng,
    city,
    state,
  }));

  _byState = {};
  _byPrefix = {};
  for (const p of _prefixes) {
    if (!_byState[p.state]) _byState[p.state] = [];
    _byState[p.state].push(p);
    _byPrefix[p.prefix] = p;
  }
}

/**
 * Get all ~930 ZIP code 3-digit prefixes with lat/lng center points.
 * @returns {Array<{prefix: string, lat: number, lng: number, city: string, state: string}>}
 */
function getZipPrefixes() {
  _buildPrefixes();
  return _prefixes;
}

/**
 * Get ZIP prefixes for a specific state code (e.g. 'NY', 'CA', 'TX').
 * @param {string} stateCode - 2-letter state code
 * @returns {Array<{prefix: string, lat: number, lng: number, city: string, state: string}>}
 */
function getZipPrefixesByState(stateCode) {
  _buildPrefixes();
  return _byState[stateCode.toUpperCase()] || [];
}

/**
 * Look up a single 3-digit prefix.
 * @param {string} prefix - 3-digit string (e.g. '100', '900')
 * @returns {{prefix: string, lat: number, lng: number, city: string, state: string} | null}
 */
function getZipPrefix(prefix) {
  _buildPrefixes();
  return _byPrefix[prefix] || null;
}

/**
 * Get all US states that have ZIP prefixes, with their prefix counts.
 * @returns {Object<string, number>} Map of state code to prefix count
 */
function getStateCounts() {
  _buildPrefixes();
  const counts = {};
  for (const [state, prefixes] of Object.entries(_byState)) {
    counts[state] = prefixes.length;
  }
  return counts;
}

/**
 * Expand prefixes into full 5-digit ZIP codes (00X00 through 00X99).
 * NOTE: Not all generated ZIPs are real — this is an exhaustive expansion.
 * Each ZIP inherits the lat/lng of its parent prefix.
 * @param {string} [stateCode] - Optional filter by state
 * @returns {Array<{zip: string, lat: number, lng: number, city: string, state: string}>}
 */
function getAllZipCodes(stateCode) {
  _buildPrefixes();
  const source = stateCode ? getZipPrefixesByState(stateCode) : _prefixes;
  const result = [];
  for (const p of source) {
    for (let i = 0; i < 100; i++) {
      const suffix = String(i).padStart(2, '0');
      result.push({
        zip: p.prefix + suffix,
        lat: p.lat,
        lng: p.lng,
        city: p.city,
        state: p.state,
      });
    }
  }
  return result;
}

/**
 * Get unique city/state pairs with their coordinates — useful as search
 * center points for Google Maps scraper. Deduplicates cities that appear
 * across multiple prefixes.
 * @param {string} [stateCode] - Optional filter by state
 * @returns {Array<{city: string, state: string, lat: number, lng: number, prefixCount: number}>}
 */
function getUniqueCities(stateCode) {
  _buildPrefixes();
  const source = stateCode ? getZipPrefixesByState(stateCode) : _prefixes;
  const seen = new Map();
  for (const p of source) {
    const key = `${p.city}|${p.state}`;
    if (!seen.has(key)) {
      seen.set(key, { city: p.city, state: p.state, lat: p.lat, lng: p.lng, prefixCount: 1 });
    } else {
      seen.get(key).prefixCount++;
    }
  }
  return Array.from(seen.values()).sort((a, b) => b.prefixCount - a.prefixCount);
}

/**
 * Get ZIP prefix data suitable for Google Maps geo-grid searches.
 * Returns center points spaced across the US, filtered to exclude
 * military (AE/AP), territories (PR/VI/GU), and optionally a specific state.
 * @param {Object} [options]
 * @param {string} [options.state] - Filter to a single state
 * @param {boolean} [options.includeTerrritories=false] - Include PR, VI, GU
 * @param {boolean} [options.includeMilitary=false] - Include AE, AP
 * @returns {Array<{prefix: string, lat: number, lng: number, city: string, state: string}>}
 */
function getSearchPoints(options = {}) {
  _buildPrefixes();
  const { state, includeTerritories = false, includeMilitary = false } = options;
  const militaryCodes = new Set(['AE', 'AP', 'AA']);
  const territoryCodes = new Set(['PR', 'VI', 'GU', 'AS', 'MH', 'FM', 'PW', 'MP']);

  let source = state ? getZipPrefixesByState(state) : _prefixes;
  return source.filter(p => {
    if (!includeMilitary && militaryCodes.has(p.state)) return false;
    if (!includeTerritories && territoryCodes.has(p.state)) return false;
    return true;
  });
}

// ---------------------------------------------------------------------------
// Cache-based full ZIP code dataset (fetches from API on first call)
// ---------------------------------------------------------------------------
const CACHE_PATH = path.join(__dirname, '..', 'data', 'us-zipcodes.json');

/**
 * Get a comprehensive ZIP code dataset. On first call, checks for a cached
 * JSON file at data/us-zipcodes.json. If not found, expands from prefixes.
 * You can populate the cache with real ZIP data from an external source
 * by placing a JSON array of {zip, lat, lng, city, state} at that path.
 *
 * @returns {Array<{zip: string, lat: number, lng: number, city: string, state: string}>}
 */
function getCachedZipCodes() {
  // Try to load from cache
  try {
    if (fs.existsSync(CACHE_PATH)) {
      const data = JSON.parse(fs.readFileSync(CACHE_PATH, 'utf8'));
      if (Array.isArray(data) && data.length > 0) {
        return data;
      }
    }
  } catch (e) {
    // Cache corrupted or missing, fall through to prefix expansion
  }

  // Fall back to prefix expansion
  return getAllZipCodes();
}

/**
 * Save a ZIP code dataset to the cache file.
 * @param {Array<{zip: string, lat: number, lng: number, city: string, state: string}>} data
 */
function saveCacheFile(data) {
  const dir = path.dirname(CACHE_PATH);
  if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
  fs.writeFileSync(CACHE_PATH, JSON.stringify(data), 'utf8');
}

/**
 * Fetch real ZIP code data from a free API and cache it.
 * Uses the free dataset at the given URL (defaults to a public GitHub dataset).
 * @param {string} [url] - URL to fetch ZIP data from
 * @returns {Promise<Array<{zip: string, lat: number, lng: number, city: string, state: string}>>}
 */
async function fetchAndCacheZipCodes(url) {
  const https = require('https');
  const http = require('http');

  const fetchUrl = url || 'https://raw.githubusercontent.com/millbj92/US-Zipcode-Database/master/us-zipcodes.json';

  return new Promise((resolve, reject) => {
    const client = fetchUrl.startsWith('https') ? https : http;
    client.get(fetchUrl, { headers: { 'User-Agent': 'mortar-lead-scraper/1.0' } }, (res) => {
      if (res.statusCode === 301 || res.statusCode === 302) {
        // Follow redirect
        return fetchAndCacheZipCodes(res.headers.location).then(resolve).catch(reject);
      }
      if (res.statusCode !== 200) {
        return reject(new Error(`HTTP ${res.statusCode} fetching ZIP codes`));
      }

      let body = '';
      res.on('data', chunk => { body += chunk; });
      res.on('end', () => {
        try {
          const raw = JSON.parse(body);
          // Normalize to our format (different datasets have different schemas)
          const data = raw.map(r => ({
            zip: String(r.zip || r.zipcode || r.zip_code || '').padStart(5, '0'),
            lat: parseFloat(r.lat || r.latitude || r.Lat || 0),
            lng: parseFloat(r.lng || r.lon || r.longitude || r.Lng || r.Long || 0),
            city: r.city || r.primary_city || r.City || '',
            state: r.state || r.state_code || r.State || r.state_id || '',
          })).filter(r => r.zip.length === 5 && r.lat !== 0 && r.lng !== 0);

          saveCacheFile(data);
          resolve(data);
        } catch (e) {
          reject(new Error(`Failed to parse ZIP code data: ${e.message}`));
        }
      });
      res.on('error', reject);
    }).on('error', reject);
  });
}

